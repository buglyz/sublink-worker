/**
 * Download miaomiaowu ACL/Aethersailor .ini presets, convert to V3 YAML, embed as JS modules.
 * Run: node scripts/build-miaomiaowu-templates.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { ALL_INI_PRESETS } from '../src/templates/presets.js';
import { convertAclToV3, buildV3YamlFromAcl } from '../src/builders/helpers/aclToV3.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outDir = path.join(__dirname, '../src/templates/generated');

fs.mkdirSync(outDir, { recursive: true });

function safeFileId(id) {
    return String(id)
        .replace(/[^\w.-]+/g, '_')
        .replace(/^_+|_+$/g, '') || 'template';
}

const registry = [];
let ok = 0;
let fail = 0;

for (const preset of ALL_INI_PRESETS) {
    const fileId = safeFileId(preset.id);
    process.stdout.write(`fetch ${preset.id} ... `);
    try {
        const res = await fetch(preset.url, {
            headers: { 'User-Agent': 'sublink-worker-template-builder/1.0' },
            signal: AbortSignal.timeout(30000),
        });
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        const ini = await res.text();
        if (!ini.includes('ruleset=') && !ini.includes('custom_proxy_group=')) {
            throw new Error('not an ACL-style ini');
        }
        const v3 = convertAclToV3(ini);
        if (!v3.proxyGroups.length) throw new Error('no proxy groups after convert');
        const yamlText = buildV3YamlFromAcl(v3, { dnsMode: 'fake-ip', name: preset.id });
        const modulePath = path.join(outDir, `${fileId}.js`);
        const body =
            `// Auto-generated from ${preset.url}\n` +
            `// ${preset.label}\n` +
            `export default ${JSON.stringify(yamlText)};\n`;
        fs.writeFileSync(modulePath, body, 'utf8');
        registry.push({
            id: preset.id,
            fileId,
            label: preset.label,
            source: 'acl-ini',
            groups: v3.proxyGroups.length,
            rules: v3.rules.length,
            providers: Object.keys(v3.ruleProviders).length,
        });
        ok++;
        console.log(`ok (groups=${v3.proxyGroups.length} rules=${v3.rules.length})`);
    } catch (err) {
        fail++;
        console.log(`FAIL ${err.message}`);
    }
}

const indexLines = [
    '// Auto-generated by scripts/build-miaomiaowu-templates.mjs — do not edit by hand',
    ...registry.map(
        (r) => `import t_${r.fileId} from './${r.fileId}.js';`
    ),
    '',
    'export const GENERATED_TEMPLATES = {',
    ...registry.map((r) => `  ${JSON.stringify(r.id)}: t_${r.fileId},`),
    '};',
    '',
    'export const GENERATED_META = {',
    ...registry.map(
        (r) =>
            `  ${JSON.stringify(r.id)}: ${JSON.stringify({ label: r.label, source: r.source })},`
    ),
    '};',
    '',
];

fs.writeFileSync(path.join(outDir, 'index.js'), indexLines.join('\n'), 'utf8');
fs.writeFileSync(
    path.join(outDir, 'manifest.json'),
    JSON.stringify({ generatedAt: new Date().toISOString(), ok, fail, templates: registry }, null, 2)
);

console.log(`\nDone: ${ok} ok, ${fail} fail → ${outDir}`);
if (fail > 0) process.exitCode = 1;
